//arrow function to add 2 numbers

var addnums= (num1,num2)=> num1+num2

console.log(addnums(2,9));

// arrow function to find product

var product=(n1,n2)=>n1*n2

console.log(product(3,5));

// to find cube of a number

var cube=(n1)=> n1**3
console.log(cube(5));

//largest of 2 numbers

var largest=(n1,n2)=> n1>n2 ? n1:n2

console.log(largest(8,14));
